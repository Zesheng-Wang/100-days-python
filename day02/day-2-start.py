print("Hello World")

# 字符串索引
print("Hello World"[0])

# 整数
print(123 + 321)

# 浮点数
print(3.24 + 2)

# 布尔类型
True
False

# 运算符
print("1 + 1 = ", 1 + 1)  # 加法
print("2 - 4 = ", 2 - 4)  # 减法
print("2 * 3 = ", 2 * 3)  # 乘法
print("4 / 2 = ", 4 / 2)  # 除法
print("5 % 2 = ", 5 % 2)  # 摸除(取余数)

# 类型转换
print(type("Hello"))
print(type(123))
print(type(3.14))
print(type(True))

# 错误示例
print(3 + '3')

# str()函数
print(str(3) + '3')

# f-Strings
name = 'Michael'
age = 27
print(f"Hi everyone, My name is {name} and I am {age}.")
